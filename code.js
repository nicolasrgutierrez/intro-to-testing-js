function sayHello(name) {
    if ( name === true || false) {
        return "Hello, World"
    } else if (name === false) {
        return "Hello, World"
    } else if (name === "Nick" || "Alex" || "Travis" || "World") {
        return "Hello, " + name;
    }
}
























